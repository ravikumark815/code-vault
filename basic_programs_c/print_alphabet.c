#include <stdio.h>
#include <stdlib.h>

int main()
{
    
    printf("Using characters:");
    for (int i = 'A'; i <= 'Z'; i++){
        printf("%c", i);
    }
    
    printf("\nUsing ASCII:");
    for (int i = 65; i <= 90; i++){
        printf("%c", i);
    }
    printf("\n");

    return 0;
}